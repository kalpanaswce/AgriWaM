  /// Get precipitation for a specific day of year (1-366)
  static double getPrecipitation(int dayOfYear) {
    
    if (dayOfYear < 1 || dayOfYear > 366) {
      throw ArgumentError('Day of year must be between 1 and 366, got: $dayOfYear');
    }
    
    return _precipitationData[dayOfYear];
  }
  
  /// Get precipitation for a specific date
  static double? getPrecipitationForDate(DateTime date) {
    final dayOfYear = _getDayOfYear(date);
    return getPrecipitation(dayOfYear);
  }
  
  /// Convert DateTime to day of year (1-366)
  static int _getDayOfYear(DateTime date) {
    final startOfYear = DateTime(date.year, 1, 1);
    final difference = date.difference(startOfYear);
    return difference.inDays + 1;
  }
